#pragma once

#include <QTextBrowser>
#include <QVBoxLayout>
#include <QWidget>

class HelpWindow : public QWidget {
   public:
    explicit HelpWindow(QWidget* parent = nullptr) : QWidget(parent) {
        setWindowTitle("Помощь");
        resize(800, 600);

        auto* layout = new QVBoxLayout(this);

        auto* textBrowser = new QTextBrowser(this);
        textBrowser->setMarkdown(HELP_MESSAGE);
        textBrowser->setReadOnly(true);

        layout->addWidget(textBrowser);
    }

   private:
    const QString HELP_MESSAGE = R"(
Инструкция по работе с программой

Начало работы

При запуске поле ввода заблокировано, для начала работы возможны два варианта действий:

1. нажать «Файл» - «Создать» и ввести строку, удовлетворяющую следующим условиям:
    - строка содержит только прописные русские буквы и символы «_», заменяющие пробел;
    - максимальная длина введенной строки - 30 символов.

2. Нажать «Файл» - «Открыть», что дает возможность открыть уже существующий файл.

Применение алгоритмов зашифрования и расшифрования

Алгоритм шифрования может быть применен при выборе ключа шифрования.

Ключом шифра является строка размером d из неповторяющихся цифр (от 1 до d)

Зашифрование

1. ввести открытый текст с клавиатуры или загрузить из файла;
2. выбрать метод зашифрования в выпадающем меню, нажмите «Зашифровать» - «Посимвольная»;
3. ввести значение ключа, нажать «ОК»;
4. результат появится в новой строке.

Расшифрования

1. ввести шифротекст или загрузить из файла;
2. выбрать метод расшифрования в выпадающем меню, нажмите «Расшифровать» - «Посимвольная»;  
3. ввести значение ключа, нажать «ОК»;
4. Результат появится в новой строке

Работа с файлами

В меню «Файл» представлены следующие опции:

1. «Создать» - очистка поля и предоставление доступа ввода текста;
2. «Открыть» - загрузка текста из уже существующего файла с расширением .txt;
3. «Сохранить» - сохранение последней непустой строки в файл, доступна только при предварительном открытии файла;
4. «Сохранить как» - сохранение последней строчки в новый файл.

Описание метода посимвольной векторной перестановки.

При шифровании посимвольной векторной перестановкой исходный текст разбивается на блоки фиксированной длины, и символы внутри каждого блока переставляются по определенному правилу, задаваемому ключом.

Зашифрование происходит следующим образом:
1. исходный текст разбивается на блоки по d цифр;
2. если длина текста не кратна d, последний блок дополняется символами «_» до полной длины;
3. в каждом блоке символы переставляются согласно ключу:
    - позиция в ключе определяет новую позицию символа в зашифрованном блоке;
    - значение в ключе указывает, с какой позиции исходного блока берется символ;
4. все преобразованные блоки объединяются в зашифрованный текст.

Расшифрование происходит следующим образом:
1. зашифрованный текст разбивается на блоки по d цифр;
2. в каждом блоке выполняется обратная перестановка символов согласно ключу:
    значение в ключе указывает, на какую новую позицию в исходном блоке нужно поставить символ с текущей позиции;
3. все преобразованные блоки объединяются в расшифрованный текст.
    )";
};
